#!/usr/bin/python3

# some math operators

print(3 + 4) # addition (+)
print(3 - 4) # subtraction (-)
print(3 / 4) # division (/)
print(3 * 4) # multiplication (*)
print(3 ** 4) # exponential (**)
print(3 % 4) # modulus(%)
print(3 // 4) # floor divison operator (//)

# Checks the data type of a value

print(type(10))
print(type(9.8))
print(type(3.14))
print(type(4-4j))
print(type(['Asabeneh','Python','Finland']))
print(type(True))
print(type("Morocco"))
print(type((1, 'hey', 10.10)))
print(type({"good": 4, "bad": 3}))

# Euclidian distance between (2, 3) and (10, 8)

